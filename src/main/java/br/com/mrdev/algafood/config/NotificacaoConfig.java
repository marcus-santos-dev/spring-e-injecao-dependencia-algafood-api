import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import br.com.mrdev.algafood.di.notificacao.desambiguacao.via.list.NotificadorEmailDesambiguacaoViaList;

@Configuration
public class NotificacaoConfig {

	/**
	 * Uma observação importante.
	 * Dentro do IoC Container, por padrão o bean será nomeado como notificadorEmail que é exatamente
	 * o nome desse método que define o bean.
	 * 
	 * Essa configuracao de bean veio para essa classe para explicarmos como fazer a configuração de beans via 
	 * classes @Configuration e essas classes conseguirem uma referenciar a outra quando necessário. 
	 * 
	 */
	@Bean
	public NotificadorEmailDesambiguacaoViaList notificadorEmail() {
		NotificadorEmailDesambiguacaoViaList notificadorEmail = new NotificadorEmailDesambiguacaoViaList("smtp.algamail.com.br");
		notificadorEmail.setCaixaAlta(true);
		return notificadorEmail;
	}
	
}
