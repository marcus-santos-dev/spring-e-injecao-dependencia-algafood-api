import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

/**
 * Ao configurar o "agrupador" [@ConfigurationProperties("notificador.email")]
 * todos os atributos que iniciarem com o radical [notificador.email] serão carregados
 * por aqui.
 * 		notificador.email.host-servidor=smtp.algafood.com.br
 *		notificador.email.porta-servidor=25
 * Observação: O que é [host-servidor] vira [hostServidor] e o que é [porta-servidor] vira [portaServidor].
 */
@Component
@ConfigurationProperties("notificador.email")
public class NotificadorProperties {

	private String hostServidor;
	private Integer portaServidor;
	
	public String getHostServidor() {
		return hostServidor;
	}
	public void setHostServidor(String hostServidor) {
		this.hostServidor = hostServidor;
	}
	public Integer getPortaServidor() {
		return portaServidor;
	}
	public void setPortaServidor(Integer portaServidor) {
		this.portaServidor = portaServidor;
	}

}
